package app.entity;

public class Xueqichengji {
	double xuefen;
	double jidian;
	public Xueqichengji(double xuefen, double jidian) {
		super();
		this.xuefen = xuefen;
		this.jidian = jidian;
	}
	public double getXuefen() {
		return xuefen;
	}
	public void setXuefen(double xuefen) {
		this.xuefen = xuefen;
	}
	public double getJidian() {
		return jidian;
	}
	public void setJidian(double jidian) {
		this.jidian = jidian;
	}
    
}
